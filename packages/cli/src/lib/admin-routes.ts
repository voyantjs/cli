/**
 * The createFileRoute id for a contribution path hosted under `routesDir`
 * (relative to the config dir, e.g. "src/routes/_workspace"): the dir's
 * segments after `routes` become the tree prefix, and index files keep the
 * trailing slash (`/_workspace/promotions/`).
 */
export function fileRouteIdFor(routesDirRel: string, routePath: string): string {
  const segments = routesDirRel.split(/[\\/]/).filter(Boolean)
  const routesIndex = segments.lastIndexOf("routes")
  const prefixSegments = routesIndex === -1 ? [] : segments.slice(routesIndex + 1)
  const prefix = prefixSegments.length > 0 ? `/${prefixSegments.join("/")}` : ""
  return `${prefix}${routePath}/`
}

/** Canonical generated-file location for a contribution path: `<rel>/index.tsx`. */
export function canonicalRouteFileRelPath(routePath: string): string {
  return join(...routePath.replace(/^\//, "").split("/"), "index.tsx")
}

/**
 * Alternative file-routing spellings a hand-written host for `routePath`
 * may already use. Any existing one means the route is already bound and the
 * generator must not create a parallel file.
 */
export function alternativeRouteFileRelPaths(routePath: string): string[] {
  const rel = routePath.replace(/^\//, "")
  const dotted = rel.replaceAll("/", ".")
  const segments = rel.split("/")
  return [
    `${join(...segments)}.tsx`,
    `${join(...segments)}.ts`,
    join(...segments, "index.ts"),
    join(...segments, "route.tsx"),
    `${dotted}.tsx`,
    `${dotted}.index.tsx`,
  ]
}

export interface RenderRouteFileOptions {
  /** createFileRoute id, e.g. `/_workspace/promotions/`. */
  fileRouteId: string
  /** Admin entry import specifier, e.g. `@voyantjs/promotions-react/admin`. */
  importSpec: string
  /** Extension factory export, e.g. `createPromotionsAdminExtension`. */
  exportName: string
  /** Route contribution id, e.g. `promotions-index`. */
  routeId: string
  ssr: boolean | "data-only" | null
  preload: string | false | null
  hasLoader: boolean
  hasValidateSearch: boolean
  runtime: AdminRouteRuntimeImports
}

/**
 * Render a generated thin route file: resolve the contribution through
 * `requireAdminRoute` and bind it to the host's file-based route tree with
 * the app runtime â€” exactly what the hand-written thin hosts do. Output is
 * deterministic so `--check` stays a pure string comparison.
 */
export function renderRouteFile(options: RenderRouteFileOptions): string {
  const { fileRouteId, importSpec, exportName, routeId, ssr, preload, runtime } = options

  const imports = [
    `import { createFileRoute } from "@tanstack/react-router"`,
    `import { requireAdminRoute } from "@voyantjs/admin"`,
    `import { ${exportName} } from "${importSpec}"`,
  ]
  if (options.hasLoader) {
    imports.push(
      `import { ${runtime.apiUrlExport} } from "${runtime.apiUrlModule}"`,
      `import { ${runtime.fetcherExport} } from "${runtime.fetcherModule}"`,
    )
  }

  const routeOptions: string[] = []
  if (ssr !== null) {
    routeOptions.push(`  ssr: ${typeof ssr === "string" ? JSON.stringify(ssr) : String(ssr)},`)
  }
  if (preload !== null) {
    routeOptions.push(`  preload: ${preload === false ? "false" : JSON.stringify(preload)},`)
  }
  if (options.hasValidateSearch) {
    routeOptions.push(`  validateSearch: route.validateSearch,`)
  }
  if (options.hasLoader) {
    routeOptions.push(
      `  loader: ({ context }) =>`,
      `    route.loader?.({`,
      `      queryClient: context.queryClient,`,
      `      runtime: { baseUrl: ${runtime.apiUrlExport}(), fetcher: ${runtime.fetcherExport} },`,
      `    }),`,
    )
  }
  routeOptions.push(`  component: RouteComponent,`)

  return [
    `${GENERATED_ROUTE_HEADER} â€” do not edit.`,
    `// Thin host for the package-delivered ${importSpec} route "${routeId}"`,
    `// (packaged-admin RFC Â§4.2): page, loader, and SSR mode are package-owned;`,
    `// this file only binds them into the host's file-based route tree with the`,
    `// app runtime. Recreate with \`voyant admin generate --routes\`.`,
    `// To eject and hand-maintain this host, delete this header â€” the generator`,
    `// skips files without it.`,
    ...imports,
    ``,
    `const route = requireAdminRoute(${exportName}(), "${routeId}")`,
    `// Top-level identifier on purpose: the router's code-splitter can extract`,
    `// an identifier binding, but not a member expression.`,
    `const RouteComponent = route.component`,
    ``,
    `export const Route = createFileRoute("${fileRouteId}")({`,
    ...routeOptions,
    `})`,
    ``,
  ].join("\n")
}

