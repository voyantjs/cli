/**
 * Static analysis + rendering for `voyant admin generate --routes` and the
 * admin doctor's destination-parity finding (packaged-admin RFC Â§4.2 / Â§4.7).
 *
 * Everything here is pure source-text scanning â€” no admin entry is ever
 * imported or executed. The scanners are deliberately convention-bound: they
 * understand the `defineAdminExtension({ routes: [...] })` shape and the
 * `declare module "@voyantjs/admin"` destination declarations the `*-react`
 * packages actually write, resolving option-destructuring string defaults
 * (`const { path = "/promotions" } = options`) and template-literal paths
 * (`` `${basePath}/contracts` ``) against those defaults.
 */

/** First line of every generated route file â€” its presence marks the file as generator-owned. */
export const GENERATED_ROUTE_HEADER = "// GENERATED by voyant admin generate --routes"

/** Is this route-file content generator-owned (vs hand-written/ejected)? */
export function isGeneratedRouteFile(content: string): boolean {
  return content.startsWith(GENERATED_ROUTE_HEADER)
}

/** One route contribution statically extracted from an admin entry source. */
export interface ScannedRouteContribution {
  /** `id:` string literal, or null when not statically resolvable. */
  id: string | null
  /** Resolved route path, or null when not statically resolvable. */
  path: string | null
  /** Raw text of the path value (for skip reporting when unresolvable). */
  rawPath: string | null
  hasComponent: boolean
  /** A lazy `page:` module loader is present (packaged-admin RFC Â§4.8). */
  hasPage: boolean
  hasLoader: boolean
  hasValidateSearch: boolean
  /** Raw text of the `validateSearch:` value, for schema-identifier resolution. */
  validateSearchRaw: string | null
  /** `ssr:` literal when statically present (`true`/`false`/`"data-only"`). */
  ssr: boolean | "data-only" | null
  /** `preload:` literal when statically present (`false` or a policy string). */
  preload: string | false | null
  /**
   * `destination:` string literal â€” the semantic destination key this route
   * satisfies (packaged-admin RFC Â§4.7 endgame) â€” or null when absent / not
   * statically resolvable.
   */
  destination: string | null
  /**
   * `destinationParams:` object literal mapping route param name â†’
   * destination param name, or null when absent / not statically resolvable.
   */
  destinationParams: Record<string, string> | null
}

/** Does the contribution carry an implementation (`page` or `component`)? */
export function isImplementedContribution(contribution: ScannedRouteContribution): boolean {
  return contribution.hasPage || contribution.hasComponent
}

