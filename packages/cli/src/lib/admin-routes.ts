/**
 * Destination keys an admin entry declares via
 * `declare module "@voyantjs/admin" { interface AdminDestinations { ... } }`.
 */
export function scanDeclaredDestinationKeys(source: string): string[] {
  const keys = new Set<string>()
  const modulePattern = /declare\s+module\s+["']@voyantjs\/admin["']/g
  let moduleMatch: RegExpExecArray | null = modulePattern.exec(source)
  while (moduleMatch !== null) {
    const moduleOpen = source.indexOf("{", moduleMatch.index + moduleMatch[0].length - 1)
    if (moduleOpen !== -1) {
      const moduleClose = findMatching(source, moduleOpen)
      if (moduleClose !== -1) {
        const moduleBody = source.slice(moduleOpen + 1, moduleClose)
        const interfaceMatch = /interface\s+AdminDestinations\b/.exec(moduleBody)
        if (interfaceMatch) {
          const interfaceOpen = moduleBody.indexOf(
            "{",
            interfaceMatch.index + interfaceMatch[0].length,
          )
          if (interfaceOpen !== -1) {
            const interfaceClose = findMatching(moduleBody, interfaceOpen)
            if (interfaceClose !== -1) {
              for (const key of scanTopLevelQuotedKeys(
                moduleBody.slice(interfaceOpen + 1, interfaceClose),
              )) {
                keys.add(key)
              }
            }
          }
        }
      }
    }
    moduleMatch = modulePattern.exec(source)
  }
  return [...keys]
}

/**
 * Keys of the host's destination resolver map â€” the object literal followed
 * by `satisfies AdminDestinationResolvers`. Returns null when no such map is
 * found in the source (so callers can tell "no map" apart from "empty map").
 */
export function scanResolverMapKeys(source: string): string[] | null {
  const pattern = /=\s*\{/g
  let match: RegExpExecArray | null = pattern.exec(source)
  while (match !== null) {
    const open = match.index + match[0].length - 1
    const close = findMatching(source, open)
    if (close !== -1) {
      const after = source.slice(close + 1)
      if (/^\s*satisfies\s+AdminDestinationResolvers\b/.test(after)) {
        return scanTopLevelQuotedKeys(source.slice(open + 1, close))
      }
    }
    match = pattern.exec(source)
  }
  return null
}

