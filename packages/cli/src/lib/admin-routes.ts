// ---------------------------------------------------------------------------
// Tokenizer-lite: balanced scanning that skips strings, templates, comments.
// ---------------------------------------------------------------------------

/** Index just past a quoted string starting at `start` (a `'` or `"`). */
function skipQuoted(source: string, start: number): number {
  const quote = source[start]
  let i = start + 1
  while (i < source.length) {
    const ch = source[i]
    if (ch === "\\") {
      i += 2
      continue
    }
    if (ch === quote) return i + 1
    i++
  }
  return source.length
}

/** Index just past a template literal starting at `start` (a backtick). */
function skipTemplate(source: string, start: number): number {
  let i = start + 1
  while (i < source.length) {
    const ch = source[i]
    if (ch === "\\") {
      i += 2
      continue
    }
    if (ch === "`") return i + 1
    if (ch === "$" && source[i + 1] === "{") {
      const close = findMatching(source, i + 1)
      i = close === -1 ? source.length : close + 1
      continue
    }
    i++
  }
  return source.length
}

/** Drop leading line/block comments (and surrounding whitespace) from `text`. */
function stripLeadingComments(text: string): string {
  let i = 0
  while (i < text.length) {
    if (/\s/.test(text[i] as string)) {
      i++
      continue
    }
    const skipped = skipComment(text, i)
    if (skipped === i) break
    i = skipped
  }
  return text.slice(i)
}

/** Index just past a comment starting at `start`, or `start` when not a comment. */
function skipComment(source: string, start: number): number {
  if (source[start] !== "/") return start
  const next = source[start + 1]
  if (next === "/") {
    const nl = source.indexOf("\n", start)
    return nl === -1 ? source.length : nl + 1
  }
  if (next === "*") {
    const end = source.indexOf("*/", start + 2)
    return end === -1 ? source.length : end + 2
  }
  return start
}

/**
 * Index of the bracket matching the opener at `openIndex` (`{`, `[`, `(`),
 * or -1. Skips strings, template literals (including their interpolations),
 * and comments.
 */
export function findMatching(source: string, openIndex: number): number {
  const open = source[openIndex]
  const close = open === "{" ? "}" : open === "[" ? "]" : open === "(" ? ")" : null
  if (close === null) return -1
  let depth = 0
  let i = openIndex
  while (i < source.length) {
    const ch = source[i]
    if (ch === '"' || ch === "'") {
      i = skipQuoted(source, i)
      continue
    }
    if (ch === "`") {
      i = skipTemplate(source, i)
      continue
    }
    if (ch === "/") {
      const skipped = skipComment(source, i)
      if (skipped !== i) {
        i = skipped
        continue
      }
    }
    if (ch === open) depth++
    else if (ch === close) {
      depth--
      if (depth === 0) return i
    }
    i++
  }
  return -1
}

/**
 * Split an object-literal body on top-level commas, skipping strings,
 * templates, comments, and anything nested in `()[]{}`.
 */
function splitTopLevel(body: string): string[] {
  const parts: string[] = []
  let depth = 0
  let last = 0
  let i = 0
  while (i < body.length) {
    const ch = body[i]
    if (ch === '"' || ch === "'") {
      i = skipQuoted(body, i)
      continue
    }
    if (ch === "`") {
      i = skipTemplate(body, i)
      continue
    }
    if (ch === "/") {
      const skipped = skipComment(body, i)
      if (skipped !== i) {
        i = skipped
        continue
      }
    }
    if (ch === "(" || ch === "[" || ch === "{") depth++
    else if (ch === ")" || ch === "]" || ch === "}") depth--
    else if (ch === "," && depth === 0) {
      parts.push(body.slice(last, i))
      last = i + 1
    }
    i++
  }
  parts.push(body.slice(last))
  return parts.map((part) => part.trim()).filter((part) => part.length > 0)
}

/**
 * Quoted keys (`"x.y":` / `"x.y"?:`) at brace-depth 0 of an interface or
 * object-literal body. Strings in value position (no following colon) and
 * anything nested are ignored.
 */
function scanTopLevelQuotedKeys(body: string): string[] {
  const keys: string[] = []
  let depth = 0
  let i = 0
  while (i < body.length) {
    const ch = body[i]
    if (ch === '"' || ch === "'") {
      const end = skipQuoted(body, i)
      if (depth === 0) {
        const key = body.slice(i + 1, end - 1)
        let j = end
        while (j < body.length && /\s/.test(body[j] as string)) j++
        if (body[j] === "?") j++
        while (j < body.length && /\s/.test(body[j] as string)) j++
        if (body[j] === ":") keys.push(key)
      }
      i = end
      continue
    }
    if (ch === "`") {
      i = skipTemplate(body, i)
      continue
    }
    if (ch === "/") {
      const skipped = skipComment(body, i)
      if (skipped !== i) {
        i = skipped
        continue
      }
    }
    if (ch === "(" || ch === "[" || ch === "{") depth++
    else if (ch === ")" || ch === "]" || ch === "}") depth--
    i++
  }
  return keys
}

