describe("adminGenerateCommand --routes --files (legacy thin files)", () => {
  let tmp: string

  beforeEach(() => {
    tmp = mkdtempSync(join(tmpdir(), "voyant-cli-admin-routes-"))
  })

  afterEach(() => {
    rmSync(tmp, { recursive: true, force: true })
  })

  const generatedPath = () => join(tmp, "src", "routes", "_workspace", "foo", "index.tsx")

  it("emits a generated thin route file for a zero-prop contribution only", async () => {
    writeRoutesFixture(tmp)
    const { ctx, stdout } = makeCtx(["--routes", "--files"], tmp)
    const code = await adminGenerateCommand(ctx)
    expect(code).toBe(0)

    const content = readFileSync(generatedPath(), "utf8")
    expect(content.startsWith("// GENERATED by voyant admin generate --routes")).toBe(true)
    expect(content).toContain(`import { requireAdminRoute } from "@voyantjs/admin"`)
    expect(content).toContain(`import { createFooAdminExtension } from "@voyantjs/foo-react/admin"`)
    expect(content).toContain(`import { getApiUrl } from "@/lib/env"`)
    expect(content).toContain(`import { operatorFetcher } from "@/lib/voyant-fetcher"`)
    expect(content).toContain(
      `const route = requireAdminRoute(createFooAdminExtension(), "foo-index")`,
    )
    expect(content).toContain(`createFileRoute("/_workspace/foo/")`)
    expect(content).toContain(`ssr: "data-only",`)
    expect(content).toContain(`component: RouteComponent,`)

    // The param-taking detail host and the metadata-only contribution are not emitted.
    expect(existsSync(join(tmp, "src", "routes", "_workspace", "foo", "$fooId.tsx"))).toBe(false)
    expect(existsSync(join(tmp, "src", "routes", "_workspace", "foo-meta"))).toBe(false)
    expect(stdout.join("")).toContain(
      "1 param route(s) and 1 metadata-only contribution(s) left to hand-written hosts",
    )
    expect(stdout.join("")).toContain("1 written")
  })

  it("honors the manifest admin.routes runtime-import config", async () => {
    writeRoutesFixture(tmp)
    writeFileSync(
      join(tmp, "voyant.config.ts"),
      [
        `export default {`,
        `  modules: ["@voyantjs/foo"],`,
        `  admin: {`,
        `    routes: {`,
        `      apiUrlModule: "~/runtime/env",`,
        `      apiUrlExport: "apiUrl",`,
        `      fetcherModule: "~/runtime/fetch",`,
        `      fetcherExport: "appFetcher",`,
        `    },`,
        `  },`,
        `}`,
        ``,
      ].join("\n"),
    )
    const { ctx } = makeCtx(["--routes", "--files"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
    const content = readFileSync(generatedPath(), "utf8")
    expect(content).toContain(`import { apiUrl } from "~/runtime/env"`)
    expect(content).toContain(`import { appFetcher } from "~/runtime/fetch"`)
    expect(content).toContain(`runtime: { baseUrl: apiUrl(), fetcher: appFetcher },`)
  })

  it("never overwrites a hand-written file lacking the generated header (ejection)", async () => {
    writeRoutesFixture(tmp)
    mkdirSync(join(tmp, "src", "routes", "_workspace", "foo"), { recursive: true })
    const handWritten = `// hand-written host\nexport const Route = {}\n`
    writeFileSync(generatedPath(), handWritten)

    const { ctx, stderr, stdout } = makeCtx(["--routes", "--files"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
    expect(readFileSync(generatedPath(), "utf8")).toBe(handWritten)
    expect(stderr.join("")).toContain("ejected, hand-written host")
    expect(stdout.join("")).toContain("1 ejected")
  })

  it("skips when a hand-written host exists under another file-routing spelling", async () => {
    writeRoutesFixture(tmp)
    mkdirSync(join(tmp, "src", "routes", "_workspace"), { recursive: true })
    writeFileSync(
      join(tmp, "src", "routes", "_workspace", "foo.tsx"),
      `// flat hand-written host\n`,
    )

    const { ctx, stderr } = makeCtx(["--routes", "--files"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
    expect(existsSync(generatedPath())).toBe(false)
    expect(stderr.join("")).toContain("already binds this route")
  })

  it("--check reports drift for missing and stale files, passes when regenerated", async () => {
    writeRoutesFixture(tmp)

    const missing = makeCtx(["--routes", "--files", "--check"], tmp)
    expect(await adminGenerateCommand(missing.ctx)).toBe(1)
    expect(missing.stderr.join("")).toContain("is missing")
    expect(existsSync(generatedPath())).toBe(false)

    expect(await adminGenerateCommand(makeCtx(["--routes", "--files"], tmp).ctx)).toBe(0)
    const clean = makeCtx(["--routes", "--files", "--check"], tmp)
    expect(await adminGenerateCommand(clean.ctx)).toBe(0)
    expect(clean.stdout.join("")).toContain("1 up to date")

    writeFileSync(
      generatedPath(),
      `// GENERATED by voyant admin generate --routes — do not edit.\n// stale\n`,
    )
    const drift = makeCtx(["--routes", "--files", "--check"], tmp)
    expect(await adminGenerateCommand(drift.ctx)).toBe(1)
    expect(drift.stderr.join("")).toContain("is out of date")
  })

  it("--check treats ejected files as fine", async () => {
    writeRoutesFixture(tmp)
    mkdirSync(join(tmp, "src", "routes", "_workspace", "foo"), { recursive: true })
    writeFileSync(generatedPath(), `// hand-written host\n`)
    const { ctx } = makeCtx(["--routes", "--files", "--check"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
  })

  it("respects --routes-dir", async () => {
    writeRoutesFixture(tmp)
    const { ctx } = makeCtx(["--routes", "--files", "--routes-dir", "app/routes/admin"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
    const content = readFileSync(join(tmp, "app", "routes", "admin", "foo", "index.tsx"), "utf8")
    expect(content).toContain(`createFileRoute("/admin/foo/")`)
  })
})

