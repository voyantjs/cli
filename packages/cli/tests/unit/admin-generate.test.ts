describe("adminGenerateCommand --routes (code-assembled module)", () => {
  let tmp: string

  beforeEach(() => {
    tmp = mkdtempSync(join(tmpdir(), "voyant-cli-admin-routes-module-"))
  })

  afterEach(() => {
    rmSync(tmp, { recursive: true, force: true })
  })

  const modulePath = () => join(tmp, "src", "admin.routes.generated.tsx")

  it("emits one code-assembled module — param routes included, metadata-only skipped", async () => {
    writeModuleFixture(tmp)
    const { ctx, stdout } = makeCtx(["--routes"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)

    const content = readFileSync(modulePath(), "utf8")
    expect(
      content.startsWith("// GENERATED by voyant admin generate --routes — do not edit."),
    ).toBe(true)
    expect(content).toContain(`import { createRoute } from "@tanstack/react-router"`)
    expect(content).toContain(`import { adminExtensionRouteOptions } from "@voyantjs/admin-app"`)
    expect(content).toContain(`import { fooSearchSchema } from "@voyantjs/foo-react/admin"`)
    expect(content).toContain(`import { adminExtensions } from "@/lib/admin-extensions"`)
    expect(content).toContain(`import { Route as WorkspaceRoute } from "@/routes/_workspace/route"`)
    expect(content).toContain(`const fooExtension = extension("foo")`)
    expect(content).toContain("export const FooIndexRoute = createRoute({")
    expect(content).toContain("  validateSearch: fooSearchSchema,")
    expect(content).toContain(
      `  ...adminExtensionRouteOptions(fooExtension, "foo-index", runtime),`,
    )
    // The param-taking detail route is code-assembled too — no host file needed.
    expect(content).toContain("export const FooDetailRoute = createRoute({")
    expect(content).toContain(`  path: "/foo/$fooId",`)
    // The metadata-only contribution stays out of the module.
    expect(content).not.toContain("/foo-meta")
    expect(content).toContain(`  "/foo/$fooId": typeof FooDetailRoute`)
    expect(content).toContain(`  "/_workspace/foo/$fooId": typeof FooDetailRoute`)
    expect(content).toContain(`[admin.routes.generated] No registered admin extension "\${id}".`)
    // No per-route files are written.
    expect(existsSync(join(tmp, "src", "routes"))).toBe(false)
    expect(stdout.join("")).toContain("2 extension route(s) across 1 extension(s)")
    expect(stdout.join("")).toContain("1 metadata-only contribution(s)")
  })

  it("--check reports missing and drift, passes once regenerated", async () => {
    writeModuleFixture(tmp)

    const missing = makeCtx(["--routes", "--check"], tmp)
    expect(await adminGenerateCommand(missing.ctx)).toBe(1)
    expect(missing.stderr.join("")).toContain("is missing")
    expect(existsSync(modulePath())).toBe(false)

    expect(await adminGenerateCommand(makeCtx(["--routes"], tmp).ctx)).toBe(0)
    const clean = makeCtx(["--routes", "--check"], tmp)
    expect(await adminGenerateCommand(clean.ctx)).toBe(0)
    expect(clean.stdout.join("")).toContain("is up to date")

    writeFileSync(
      modulePath(),
      `// GENERATED by voyant admin generate --routes — do not edit.\n// stale\n`,
    )
    const drift = makeCtx(["--routes", "--check"], tmp)
    expect(await adminGenerateCommand(drift.ctx)).toBe(1)
    expect(drift.stderr.join("")).toContain("is out of date")
  })

  it("never overwrites a host-owned module lacking the generated header", async () => {
    writeModuleFixture(tmp)
    mkdirSync(join(tmp, "src"), { recursive: true })
    const handWritten = `// hand-written admin routes\nexport const adminExtensionRoutes = []\n`
    writeFileSync(modulePath(), handWritten)

    const write = makeCtx(["--routes"], tmp)
    expect(await adminGenerateCommand(write.ctx)).toBe(0)
    expect(readFileSync(modulePath(), "utf8")).toBe(handWritten)
    expect(write.stderr.join("")).toContain("no generated header (ejected, host-owned)")

    // --check treats the ejected module as fine.
    const check = makeCtx(["--routes", "--check"], tmp)
    expect(await adminGenerateCommand(check.ctx)).toBe(0)
  })

  it("ejects a single route bound by a hand-written route file", async () => {
    writeModuleFixture(tmp)
    mkdirSync(join(tmp, "src", "routes", "_workspace", "foo"), { recursive: true })
    writeFileSync(
      join(tmp, "src", "routes", "_workspace", "foo", "index.tsx"),
      `// hand-written host\nexport const Route = {}\n`,
    )

    const { ctx, stderr, stdout } = makeCtx(["--routes"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
    const content = readFileSync(modulePath(), "utf8")
    expect(content).not.toContain(`path: "/foo",`)
    expect(content).toContain(`path: "/foo/$fooId",`)
    expect(stderr.join("")).toContain("binds this route (ejected)")
    expect(stdout.join("")).toContain("1 ejected")
  })

  it("removes superseded generated thin route files, and flags them with --check", async () => {
    writeModuleFixture(tmp)
    const thinFile = join(tmp, "src", "routes", "_workspace", "foo", "index.tsx")
    mkdirSync(join(thinFile, ".."), { recursive: true })
    const thinContent = `// GENERATED by voyant admin generate --routes — do not edit.\n// thin host\n`
    writeFileSync(thinFile, thinContent)

    expect(await adminGenerateCommand(makeCtx(["--routes"], tmp).ctx)).toBe(0)
    expect(existsSync(thinFile)).toBe(false)
    expect(readFileSync(modulePath(), "utf8")).toContain(`path: "/foo",`)

    writeFileSync(thinFile, thinContent)
    const check = makeCtx(["--routes", "--check"], tmp)
    expect(await adminGenerateCommand(check.ctx)).toBe(1)
    expect(check.stderr.join("")).toContain("superseded")
  })

  it("honors --routes-dir for the id prefix and the workspace route import", async () => {
    writeModuleFixture(tmp)
    const { ctx } = makeCtx(["--routes", "--routes-dir", "app/routes/admin"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
    const content = readFileSync(modulePath(), "utf8")
    expect(content).toContain(`import { Route as WorkspaceRoute } from "@/app/routes/admin/route"`)
    expect(content).toContain(`  "/admin/foo/$fooId": typeof FooDetailRoute`)
  })

  it("honors the manifest admin.routes import + out config", async () => {
    writeModuleFixture(tmp)
    writeFileSync(
      join(tmp, "voyant.config.ts"),
      [
        `export default {`,
        `  modules: ["@voyantjs/foo"],`,
        `  admin: {`,
        `    routes: {`,
        `      out: "src/admin-routes.gen.tsx",`,
        `      registryModule: "~/admin/registry",`,
        `      registryExport: "registry",`,
        `      apiUrlModule: "~/runtime/env",`,
        `      apiUrlExport: "apiUrl",`,
        `      fetcherModule: "~/runtime/fetch",`,
        `      fetcherExport: "appFetcher",`,
        `      workspaceRouteModule: "~/routes/workspace",`,
        `    },`,
        `  },`,
        `}`,
        ``,
      ].join("\n"),
    )
    const { ctx } = makeCtx(["--routes"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
    const content = readFileSync(join(tmp, "src", "admin-routes.gen.tsx"), "utf8")
    expect(content).toContain(`import { registry } from "~/admin/registry"`)
    expect(content).toContain(`import { Route as WorkspaceRoute } from "~/routes/workspace"`)
    expect(content).toContain("const runtime = () => ({ baseUrl: apiUrl(), fetcher: appFetcher })")
    expect(content).toContain("const match = registry.find((candidate) => candidate.id === id)")
    expect(content).toContain(`[admin-routes.gen] No registered admin extension "\${id}".`)
  })

  it("notes contributions whose validateSearch schema is not an export", async () => {
    writeModuleFixture(tmp)
    writeFileSync(
      join(tmp, "node_modules", "@voyantjs", "foo-react", "src", "admin", "index.tsx"),
      FOO_ADMIN_MODULE_SOURCE.replace(
        "validateSearch: (search) => fooSearchSchema.parse(search),",
        "validateSearch: (search) => privateSchema.parse(search),",
      ),
    )
    const { ctx, stderr } = makeCtx(["--routes"], tmp)
    expect(await adminGenerateCommand(ctx)).toBe(0)
    const content = readFileSync(modulePath(), "utf8")
    expect(content).not.toContain("validateSearch:")
    expect(stderr.join("")).toContain("emitted without a typed search contract")
  })
})
